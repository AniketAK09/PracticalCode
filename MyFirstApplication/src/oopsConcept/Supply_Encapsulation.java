package oopsConcept;

public class Supply_Encapsulation {
	
	private int id;
	private String name;
	private long mobNo;
	
	public void setId(int i)
	{
		id = i;
	}
	
	public int getId()
	{
		return id;
	}
	
	public void setName(String nm)
	{
		name = nm;
	}
	
	public String getName()
	{
		return name;
	}
	
	public void setMobNo(long mn)
	{
		mobNo = mn;
	}
	
	public long getMobNo()
	{
		return mobNo;
	}

}
